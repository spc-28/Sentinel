"""Compare the fine-tuned local Qwen vs Claude on held-out RCA cases.

Runs each case through both models, scores root-cause quality (keyword overlap vs the
known cause) and records cost, then writes docs/eval/finetune_results.md — including a
routing simulation (easy cases → local, hard → Claude).

Prereqs: the local model served on :8001 (serve_local.py) and ANTHROPIC_API_KEY set.
Run: `make finetune-compare` (or `python experiments/finetune/compare.py [N]`).
"""

from __future__ import annotations

import asyncio
import json
import re
import sys
from pathlib import Path

import litellm
from packages.core.config import get_settings

_ROOT = Path(__file__).resolve().parent.parent.parent
_DATA = _ROOT / "experiments" / "finetune" / "data"
_OUT = _ROOT / "docs" / "eval" / "finetune_results.md"
_STOP = {"root", "cause", "the", "a", "an", "of", "to", "in", "on", "and", "was", "is", "due"}


def _keywords(text: str) -> set[str]:
    return {w for w in re.findall(r"[a-z0-9]+", text.lower()) if len(w) > 2 and w not in _STOP}


def _overlap(expected: str, got: str) -> float:
    keys = _keywords(expected)
    return len(keys & _keywords(got)) / len(keys) if keys else 0.0


def _load_cases(limit: int) -> list[tuple[str, str, str]]:
    """(system, user, expected) from the held-out set (falls back to train)."""
    rows: list[dict] = []
    for name in ("val.jsonl", "train.jsonl"):
        path = _DATA / name
        if path.exists():
            rows += [json.loads(line) for line in path.read_text().splitlines() if line.strip()]
    cases = []
    for row in rows[:limit]:
        msgs = {m["role"]: m["content"] for m in row["messages"]}
        cases.append((msgs.get("system", ""), msgs["user"], msgs["assistant"]))
    return cases


async def _ask(model: str, system: str, user: str, **kwargs: object) -> tuple[str, float]:
    resp = await litellm.acompletion(
        model=model,
        messages=[{"role": "system", "content": system}, {"role": "user", "content": user}],
        temperature=0,
        max_tokens=512,
        **kwargs,
    )
    text = resp.choices[0].message.content or ""
    cost = float(resp._hidden_params.get("response_cost") or 0.0)  # noqa: SLF001
    return text, cost


async def main() -> None:
    settings = get_settings()
    limit = int(sys.argv[1]) if len(sys.argv) > 1 else 50
    cases = _load_cases(limit)
    if not cases:
        print("No cases — run `make training-data SYNTHETIC=50` first.")
        raise SystemExit(1)

    local_kwargs = {"api_base": settings.local_llm_base_url, "api_key": settings.local_llm_api_key}
    rows = []
    for system, user, expected in cases:
        severity = "high" if "critical" in user or "high" in user else "medium"
        try:
            local_text, _ = await _ask(settings.local_llm_model, system, user, **local_kwargs)
            claude_text, claude_cost = await _ask(settings.llm_model, system, user)
        except Exception as exc:  # noqa: BLE001
            print(f"skipped a case: {exc}")
            continue
        rows.append(
            {
                "severity": severity,
                "local_q": _overlap(expected, local_text),
                "claude_q": _overlap(expected, claude_text),
                "claude_cost": claude_cost,
            }
        )

    n = len(rows) or 1
    local_q = sum(r["local_q"] for r in rows) / n
    claude_q = sum(r["claude_q"] for r in rows) / n
    claude_cost = sum(r["claude_cost"] for r in rows)
    # Routing: easy (medium/low) → local (~$0), hard → Claude.
    routed_cost = sum(r["claude_cost"] for r in rows if r["severity"] == "high")
    routed_q = sum((r["local_q"] if r["severity"] != "high" else r["claude_q"]) for r in rows) / n

    _OUT.write_text(
        f"""# Fine-tune experiment — results

Generated by `experiments/finetune/compare.py` over {len(rows)} held-out cases.

| Setup | Root-cause quality (avg overlap) | Total cost |
| --- | --- | --- |
| Local Qwen 2.5 7B (QLoRA) | {local_q:.2f} | ~$0.00 (self-hosted) |
| Claude ({settings.llm_model}) | {claude_q:.2f} | ${claude_cost:.4f} |
| **Routed** (easy→local, hard→Claude) | {routed_q:.2f} | ${routed_cost:.4f} |

Claude cost avoided by routing: ~{100 * (1 - routed_cost / (claude_cost or 1)):.0f}%.
"""
    )
    print(f"wrote {_OUT} ({len(rows)} cases)")


if __name__ == "__main__":
    asyncio.run(main())
